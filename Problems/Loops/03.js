//  Level 3: While & Do...While
// Print numbers from 1 to 10 using a while loop.
let i = 1;
while(i <=10){
    // console.log(i);
    i++;
}

// Print numbers from 5 to 1 using a do...while loop.
let num = 1;
do{
    // console.log(num+1);
    num++;
}while(num<=5){
}

// Keep printing a number until it becomes greater than 100 (start from 1 and double it each time).
let n = 1;
while(n<=100){
    console.log(n);
    n = n*2;
}