// Switch Statement:

let fruit = "apple";

switch (fruit) {
  case "apple":
    console.log("It's red");
    break;
  case "banana":
    console.log("It's yellow");
    break;
  default:
    console.log("Unknown fruit");
}

// Take a day number (1-7) and print the day name.
let day = 7;
switch(day){
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console,log("Thursday");
    break;
  case 5:
    console.log("Friday");
    break;
  case 6:
    console.log("Saturday");
    break;
  case 7:
      console.log("Sunday");
    break;
    default:
      console.log("Invalid")
}

