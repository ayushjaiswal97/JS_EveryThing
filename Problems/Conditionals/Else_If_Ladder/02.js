// Write a grade calculator:
// 90+: A
// 75-89: B
// 50-74: C
// <50: F

let marks = 78;
if(marks > 90){
    console.log("F");
} else if(marks <= 50 && marks >=74){
    console.log("C");
}else if(marks <= 75 && marks <= 89){
    console.log("B");
} else if(marks >= 90){
    console.log("A");
}else{
    console.log("Wrong Input");
}
